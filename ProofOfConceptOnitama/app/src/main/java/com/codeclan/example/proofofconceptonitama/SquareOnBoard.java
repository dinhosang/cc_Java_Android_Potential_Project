public class SquareOnBoard {


    private int xCoord;
    private int yCoord;



    public SquareOnBoard(int x, int y){
        this.xCoord = x;
        this.yCoord = y;
    }


    public int getxCoord() {
        return xCoord;
    }

    public int getyCoord() {
        return yCoord;
    }
}
